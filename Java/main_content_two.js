// Select the element to apply the hover effect
const proPics = document.querySelector('#all_hots_pro_pics_first');
const priceElement = document.querySelector('#all_hots_price_first');
const priceDiscount = document.querySelector('#all_hots_dis_first');

// Add event listener for mouseenter
proPics.addEventListener('mouseenter', () => {
    // Apply styles when hovered over
    priceElement.style.justifyContent = 'center';
    priceElement.style.alignItems = 'center';
    priceElement.style.textAlign = 'center';
    priceElement.style.backgroundColor = '#111';
    priceElement.style.color = '#fff';
    priceElement.style.width = '125%';
    priceDiscount.style.display = 'none';

    // Uncomment the line below if you want to display the element as a block
    // priceElement.style.display = 'block';
});

// Add event listener for mouseleave
proPics.addEventListener('mouseleave', () => {
    // Reset styles when mouse leaves
    priceElement.style.justifyContent = '';
    priceElement.style.alignItems = '';
    priceElement.style.textAlign = '';
    priceElement.style.backgroundColor = '';
    priceElement.style.color = '';
    priceElement.style.width = '';
    priceDiscount.style.display = '';
    // Uncomment the line below if you want to display the element as a block
    // priceElement.style.display = '';
});


